# Operators in python

# Arithmetic Operator
# Assignment Operator
# Comparison Operator
# Logical Operator
# Identity Operator
# Membership Operator
# Bitwise Operator

print("5 + 6 is", 5+6)
print("5 - 6 is", 5-6)
print("5 * 6 is", 5*6)
print("5 / 6 is", 5/6)
print("5 ** 3 is", 5**3)
print("5 % 6 is", 5%5)
print("15 // 6 is", 15//6)

# Assignment Operator
x = 5
print(x)
x += 6 # x -= 2 % / *
print(x)

# Comparison Operator
i = 5
print(i == 5)
print(i != 5)
print(i > 5)
print(i <= 5)
print(i >= 5)
print(i and 5)

# Logical Operator
a = True
b = False
print(a or b)

# Identity Operators
y = 4
z = 5
print(y is not z)

# Membership Operator
list = [3, 5, 12, 5, 6, 4, 43, 23, 61, 7]
print(3 in list)