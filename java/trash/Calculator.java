/*write a java programme to implement a calculator and perform addition,substraction,multiplication,division and modular division*/
 class Calculator
 {
	 public static void main(String args[])
		 {
			 int n1=10,n2=20,sum=0, difference=0,product=0,quotient=0,remainder=0;
			 sum=n1+n2;
			 System.out.println("sum is:"+sum);
			 
			 difference=n1-n2;
			 System.out.println("difference is:"+difference);
			 
			 product=n1*n2;
			 System.out.println("product is:"+product);
			 
			 quotient=n1/n2;
			 System.out.println("quotient is:"+quotient);
			 
			 remainder=n1%n2;
			 System.out.println("remainder is:"+remainder);
		 }
 }
 